drop table if exists request_contact_info cascade;
drop table if exists bids cascade;

-- 1. Create Private Contact Info Table
create table request_contact_info (
  request_id bigint references service_requests(id) on delete cascade primary key,
  contact_name text,
  contact_email text,
  contact_phone text,
  exact_address text -- moving address here if needed, or keep generic city in public
);

-- Enable RLS
alter table request_contact_info enable row level security;

-- Policy 1: Homeowner (Creator) can view their own private info
create policy "Homeowner can view own private info"
on request_contact_info for select
using (
  exists (
    select 1 from service_requests
    where id = request_contact_info.request_id
    and user_id = auth.uid()
  )
);

