-- 2. Create Bids Table
create table bids (
  id bigint generated by default as identity primary key,
  request_id bigint references service_requests(id) on delete cascade not null,
  pro_id uuid references auth.users not null,
  pro_name text, -- snapshot of pro name
  pro_avatar text, 
  price_estimate text,
  message text,
  status text default 'pending', -- pending, accepted, rejected
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS
alter table bids enable row level security;

-- Policy 3: Professionals can view/edit ONLY their own bids
create policy "Pros can view own bids"
on bids for select
using (pro_id = auth.uid());

create policy "Pros can insert bids"
on bids for insert
with check (pro_id = auth.uid());

-- Policy 4: Homeowners can view ALL bids for their requests
create policy "Homeowners can view bids on their requests"
on bids for select
using (
  exists (
    select 1 from service_requests
    where id = bids.request_id
    and user_id = auth.uid()
  )
);

-- Policy 5: Homeowners can update bid status (Accept/Reject)
create policy "Homeowners can update bid status"
on bids for update
using (
  exists (
    select 1 from service_requests
    where id = bids.request_id
    and user_id = auth.uid()
  )
);


-- 3. Update Policy for Private Info (Now that bids table exists)
create policy "Accepted Pro can view private info"
on request_contact_info for select
using (
  exists (
    select 1 from bids
    where request_id = request_contact_info.request_id
    and pro_id = auth.uid()
    and status = 'accepted'
  )
);


